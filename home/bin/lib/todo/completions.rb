# frozen_string_literal: true

module Todo
  module Completions
    module Zsh
      def self.generate(commands)
        lines = []
        lines << '#compdef todo'
        lines << '# Auto-generated by: todo --completions zsh'
        lines << '# Do not edit manually.'
        lines << ''
        lines << '_todo() {'
        lines << '  local -a commands'
        lines << '  commands=('

        commands.each do |name, entry|
          comp = entry[:mod]::COMPLETIONS
          aliases = entry[:aliases]
          alias_str = aliases.empty? ? '' : " (alias: #{aliases.join(', ')})"
          lines << "    '#{name}:#{comp[:description]}#{alias_str}'"
        end

        lines << "    'help:Show help information'"
        lines << '  )'
        lines << ''
        lines << '  # First argument: command'
        lines << '  if (( CURRENT == 2 )); then'
        lines << "    _describe 'command' commands"
        lines << '    return'
        lines << '  fi'
        lines << ''
        lines << '  # Command-specific completions'
        lines << '  case "${words[2]}" in'

        commands.each do |name, entry|
          comp = entry[:mod]::COMPLETIONS
          aliases = entry[:aliases]
          pattern = ([name] + aliases).join('|')

          # Commands with subcommands (e.g., category)
          if comp[:subcommands]
            lines << "    #{pattern})"
            lines << '      local -a subcmds'
            lines << '      subcmds=('
            comp[:subcommands].each do |sub|
              sub_aliases = sub[:aliases] || []
              sub_alias_str = sub_aliases.empty? ? '' : " (alias: #{sub_aliases.join(', ')})"
              lines << "        '#{sub[:name]}:#{sub[:desc]}#{sub_alias_str}'"
            end
            lines << '      )'
            lines << '      if (( CURRENT == 3 )); then'
            lines << "        _describe 'subcommand' subcmds"
            lines << '      fi'
            lines << '      ;;'
            next
          end

          options = comp[:options] || []
          positional = comp[:positional]
          next if options.empty? && positional.nil?

          lines << "    #{pattern})"
          args = []

          # Positional argument
          case positional
          when :task_id
            args << "'1:task ID:_todo_ids'"
          when :text
            args << "'1:text:'"
          end

          # Options
          options.each do |opt|
            prefix = opt[:repeat] ? '*' : ''
            arg_spec = opt[:arg] ? arg_completion(opt[:arg]) : ''

            args << "'#{prefix}#{opt[:long]}[#{opt[:desc]}]#{arg_spec}'" if opt[:long]
            args << "'#{prefix}#{opt[:short]}[#{opt[:desc]}]#{arg_spec}'" if opt[:short]
          end

          if args.length == 1
            lines << "      _arguments #{args.first}"
          else
            lines << '      _arguments \\'
            args.each_with_index do |arg, i|
              trailing = i < args.length - 1 ? ' \\' : ''
              lines << "        #{arg}#{trailing}"
            end
          end
          lines << '      ;;'
        end

        lines << '  esac'
        lines << '}'
        lines << ''
        lines << helpers
        lines << '_todo "$@"'
        "#{lines.join("\n")}\n"
      end

      def self.arg_completion(type)
        case type
        when :category then ':category:_todo_categories'
        when :text then ':text:'
        else ''
        end
      end

      def self.helpers
        <<~'ZSH'
          # Helper: complete category names from subdirectories
          _todo_categories() {
            local conf_dir="${TODO_CONF_DIR:-$HOME/conf/todo}"
            if [[ -d "$conf_dir" ]]; then
              local -a cats
              cats=(${(f)"$(find "$conf_dir" -mindepth 1 -maxdepth 1 -type d ! -name '.*' -exec basename {} \; 2>/dev/null | sort)"})
              _describe 'category' cats
            fi
          }

          # Helper: complete task IDs from all category dirs
          _todo_ids() {
            local conf_dir="${TODO_CONF_DIR:-$HOME/conf/todo}"
            if [[ -d "$conf_dir" ]]; then
              local -a ids
              ids=(${(f)"$(find "$conf_dir" -mindepth 2 -maxdepth 2 -name '*.json' ! -name '.category.json' -exec jq -r '.[] | "\(.id):\(.description)"' {} \; 2>/dev/null | sort -t: -k1 -n)"})
              _describe 'task' ids
            fi
          }
        ZSH
      end
    end
  end
end
