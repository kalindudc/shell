# frozen_string_literal: true

module Todo
  module Completions
    module Zsh # rubocop:disable Metrics/ModuleLength
      def self.generate(commands)
        lines = header_lines(commands)
        lines.concat(case_lines(commands))
        lines << '  esac'
        lines << '}'
        lines << ''
        lines << helpers
        lines << '_todo "$@"'
        "#{lines.join("\n")}\n"
      end

      def self.header_lines(commands)
        lines = []
        lines << '#compdef todo'
        lines << '# Auto-generated by: todo --completions zsh'
        lines << '# Do not edit manually.'
        lines << ''
        lines << '_todo() {'
        lines << '  local -a commands'
        lines << '  commands=('

        commands.each do |name, entry|
          defn = entry[:mod]::DEFINITION
          aliases = defn[:aliases] || []
          alias_str = aliases.empty? ? '' : " (alias: #{aliases.join(', ')})"
          lines << "    '#{name}:#{defn[:description]}#{alias_str}'"
        end

        lines << "    'help:Show help information'"
        lines << '  )'
        lines << ''
        lines << '  # First argument: command'
        lines << '  if (( CURRENT == 2 )); then'
        lines << "    _describe 'command' commands"
        lines << '    return'
        lines << '  fi'
        lines << ''
        lines << '  # Command-specific completions'
        lines << '  case "${words[2]}" in'
        lines
      end

      def self.case_lines(commands)
        lines = []
        commands.each do |name, entry|
          defn = entry[:mod]::DEFINITION
          pattern = ([name] + (defn[:aliases] || [])).join('|')

          if defn[:subcommands]
            lines.concat(subcommand_case(pattern, defn[:subcommands]))
          else
            lines.concat(options_case(pattern, defn))
          end
        end
        lines
      end

      def self.subcommand_case(pattern, subcommands)
        lines = ["    #{pattern})", '      local -a subcmds', '      subcmds=(']
        subcommands.each do |sub|
          sub_aliases = sub[:aliases] || []
          sub_alias_str = sub_aliases.empty? ? '' : " (alias: #{sub_aliases.join(', ')})"
          lines << "        '#{sub[:name]}:#{sub[:description]}#{sub_alias_str}'"
        end
        lines << '      )'
        lines << '      if (( CURRENT == 3 )); then'
        lines << "        _describe 'subcommand' subcmds"
        lines << '      fi'
        lines << '      ;;'
        lines
      end

      def self.options_case(pattern, defn)
        options = defn[:options] || []
        positional = defn[:positional]
        return [] if options.empty? && positional.nil?

        args = positional_args(positional) + option_args(options)
        lines = ["    #{pattern})"]
        if args.length == 1
          lines << "      _arguments #{args.first}"
        else
          lines << '      _arguments \\'
          args.each_with_index do |arg, i|
            trailing = i < args.length - 1 ? ' \\' : ''
            lines << "        #{arg}#{trailing}"
          end
        end
        lines << '      ;;'
        lines
      end

      def self.positional_args(positional)
        return [] unless positional

        case positional[:type]
        when :integer then ["'1:task ID:_todo_ids'"]
        when :text then ["'1:text:'"]
        else []
        end
      end

      def self.option_args(options)
        args = []
        options.each do |opt|
          prefix = opt[:repeat] ? '*' : ''
          desc = option_desc(opt[:long])
          arg_spec = opt[:arg] ? arg_completion(opt[:arg]) : ''

          args << "'#{prefix}#{opt[:long]}[#{desc}]#{arg_spec}'" if opt[:long]
          args << "'#{prefix}#{opt[:short]}[#{desc}]#{arg_spec}'" if opt[:short]
        end
        args
      end

      def self.option_desc(long_flag)
        return '' unless long_flag

        long_flag.delete_prefix('--').tr('-', ' ').capitalize
      end

      def self.arg_completion(type)
        case type
        when :category then ':category:_todo_categories'
        when :text then ':text:'
        when :integer then ':number:'
        else ''
        end
      end

      def self.helpers
        <<~'ZSH'
          # Helper: complete category names from subdirectories
          _todo_categories() {
            local conf_dir="${TODO_CONF_DIR:-$HOME/conf/todo}"
            if [[ -d "$conf_dir" ]]; then
              local -a cats
              cats=(${(f)"$(find "$conf_dir" -mindepth 1 -maxdepth 1 -type d ! -name '.*' -exec basename {} \; 2>/dev/null | sort)"})
              _describe 'category' cats
            fi
          }

          # Helper: complete task IDs from all category dirs
          _todo_ids() {
            local conf_dir="${TODO_CONF_DIR:-$HOME/conf/todo}"
            if [[ -d "$conf_dir" ]]; then
              local -a ids
              ids=(${(f)"$(find "$conf_dir" -mindepth 2 -maxdepth 2 -name '*.json' ! -name '.category.json' -exec jq -r '.[] | "\(.id):\(.description)"' {} \; 2>/dev/null | sort -t: -k1 -n)"})
              _describe 'task' ids
            fi
          }
        ZSH
      end

      private_class_method :header_lines, :case_lines, :subcommand_case, :options_case,
                           :positional_args, :option_args, :option_desc, :arg_completion, :helpers
    end
  end
end
